```haskell
-- ============================================
-- 🐵 超やさしい Haskell サンプルまとめ
-- ============================================
-- このファイル1つで基本がなんとなく分かるようにしてるよ
-- ============================================


-- --------------------------------------------
-- ① メイン関数（ここから実行される）
-- --------------------------------------------
main :: IO ()
main = do
    putStrLn "🐵 Haskellのお勉強スタート！"

    -- 関数の実行例
    putStrLn ("2 + 3 = " ++ show (add 2 3))

    putStrLn ("5は？ -> " ++ isBig 5)
    putStrLn ("20は？ -> " ++ isBig 20)

    putStrLn ("リストの長さ = " ++ show (length numbers))

    putStrLn ("合計 = " ++ show (sumList [1,2,3,4]))

    putStrLn "🐒 おわり！"


-- --------------------------------------------
-- ② 足し算する関数
-- --------------------------------------------
-- add は「x と y をもらって足す」ルール
-- 型: Int -> Int -> Int
add :: Int -> Int -> Int
add x y = x + y


-- --------------------------------------------
-- ③ 条件分岐（if）
-- --------------------------------------------
-- 10より大きいかどうかを判定する
isBig :: Int -> String
isBig x =
    if x > 10
        then "でかい"
        else "ちいさい"


-- --------------------------------------------
-- ④ リスト（配列みたいなもの）
-- --------------------------------------------
numbers :: [Int]
numbers = [1,2,3,4]


-- --------------------------------------------
-- ⑤ 再帰（Haskellっぽい重要ポイント）
-- --------------------------------------------
-- リストの合計を出す関数
-- 空なら0
-- (x:xs) は「先頭 x と 残り xs」
sumList :: [Int] -> Int
sumList [] = 0
sumList (x:xs) = x + sumList xs


-- --------------------------------------------
-- ⑥ メモ（めちゃ大事）
-- --------------------------------------------
-- ・= は「代入」じゃなくて「定義」
-- ・値はあとから変更できない（超重要）
-- ・ループの代わりに「再帰」をよく使う
-- ・副作用が少ないのでバグりにくい
```
